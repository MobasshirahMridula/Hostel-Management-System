    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String RegNumber=jTextField1.getText();
        String name=jTextField2.getText();
        String father=jTextField3.getText();
        String mother=jTextField4.getText();
        String Roll=jTextField5.getText();
        String address=jTextField6.getText();
        String phone=jTextField7.getText();
        String email=jTextField8.getText();
        String roomNo=(String)jComboBox1.getSelectedItem();
        String status="living";
        try
        {
            Connection con=ConnectionProvider.getCon();
            PreparedStatement ps=con.prepareStatement("insert into student values(?,?,?,?,?,?,?,?,?,?)");
            ps.setString(1,RegNumber);
            ps.setString(2,name);
            ps.setString(3,father);
            ps.setString(4,mother);
            ps.setString(5,Roll);
            ps.setString(6,address);
            ps.setString(7,phone);
            ps.setString(8,email);
            ps.setString(9,roomNo);
            ps.setString(10,status);
            ps.executeUpdate();
            
            PreparedStatement ps1=con.prepareStatement("update room set roomStatus='Alloted' where number=?");
            ps1.setString(1, roomNo);
            ps1.executeUpdate();
            JOptionPane.showMessageDialog(null,"Successfully Updated");
            clear();
            
            
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

